// Create and initialize an Array
let arr = [2,4,1,5,6];

// shift one element
console.log(arr.shift());

// print remaining array
console.log(arr);

// unshift a number
console.log(arr.unshift(8));

//print array
console.log(arr);

// pop an element from array
console.log(arr.pop());

//print array
console.log(arr);

// push an element
console.log(arr.push(9));

// print element
console.log(arr);

// use splice to change value at a specific position
console.log(arr.splice(3,1,10,44));

// print the array
console.log(arr);







